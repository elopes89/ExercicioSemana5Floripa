namespace Exercicio4.Model;

public static class Calculadora
{
    public static int Soma(int numero1, int numero2)
    {
        return numero1 + numero2;
    }

    public static double Soma(double numero1, double numero2)
    {
        return numero1 + numero2;
    }

    public static float Soma(float numero1, float numero2)
    {
        return numero1 + numero2;
    }

    public static long Soma(long numero1, long numero2)
    {
        return numero1 + numero2;
    }

    public static int Subtacao(int numero1, int numero2)
    {
        return numero1 - numero2;
    }

    public static int Multiplicacao(int numero1, int numero2)
    {
        return numero1 * numero2;
    }

    public static int Divisao(int numero1, int numero2)
    {
        return numero1 / numero2;
    }
}
